package com.donkeyhouse.donkyhouse.bean;

public class Little {
    private String picihao;
    private String pound;
    private String location;
    private String jinlan;
    private String leixing;
    private String shichang;

    public Little(String picihao, String pound, String location, String jinlan, String leixing, String shichang) {
        this.picihao = picihao;
        this.pound = pound;
        this.location = location;
        this.jinlan = jinlan;
        this.leixing = leixing;
        this.shichang = shichang;
    }

    public String getPicihao() {
        return picihao;
    }

    public void setPicihao(String picihao) {
        this.picihao = picihao;
    }

    public String getPound() {
        return pound;
    }

    public void setPound(String pound) {
        this.pound = pound;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getJinlan() {
        return jinlan;
    }

    public void setJinlan(String jinlan) {
        this.jinlan = jinlan;
    }

    public String getLeixing() {
        return leixing;
    }

    public void setLeixing(String leixing) {
        this.leixing = leixing;
    }

    public String getShichang() {
        return shichang;
    }

    public void setShichang(String shichang) {
        this.shichang = shichang;
    }
}
